#!/usr/bin/env node

/**
 * Debug script to test layout and index generation
 * This will help us understand exactly what's different between JS and Go SDKs
 */

import { createSingleBlockLayout, generateIds, generateIdsFromCombined, buildIndexFile } from './dist/esm/wasm/lep1.js';
import { toBase64, fromBase64, toCanonicalJsonBytes } from './dist/esm/internal/encoding.js';
import { blake3Hash } from './dist/esm/internal/hash.js';
import fs from 'fs';

console.log('===== LAYOUT AND INDEX GENERATION DEBUG TEST =====\n');

async function testLayoutGeneration() {
  // Use the exact same test file content
  const testContent = `This is a test file for debugging index generation.
The purpose is to compare index files generated by:
1. Browser uploads (using JS SDK)
2. sn-api-manager uploads (using Go SDK)

Test timestamp: 2025-11-28 22:12:00 UTC`;

  const fileBytes = new TextEncoder().encode(testContent);

  console.log('📁 TEST FILE INFO:');
  console.log('  Content length:', fileBytes.length, 'bytes');
  console.log('  First 50 bytes (hex):',
    Array.from(fileBytes.slice(0, 50))
      .map(b => b.toString(16).padStart(2, '0'))
      .join(' ')
  );

  // Generate data hash
  const dataHash = await blake3Hash(fileBytes);
  console.log('  BLAKE3 hash:', dataHash);
  console.log();

  // Generate layout using WASM
  console.log('🔧 GENERATING LAYOUT WITH WASM:');
  const layoutBytes = await createSingleBlockLayout(fileBytes);
  console.log('  Raw WASM output length:', layoutBytes.length, 'bytes');
  console.log('  First 100 bytes (hex):',
    Array.from(layoutBytes.slice(0, 100))
      .map(b => b.toString(16).padStart(2, '0'))
      .join(' ')
  );

  // Check if WASM output is JSON
  const layoutString = new TextDecoder().decode(layoutBytes);
  console.log('  Raw WASM output (as string):');
  console.log('  ', layoutString.substring(0, 200) + '...');

  // Parse and compact the layout
  let layoutObj;
  try {
    layoutObj = JSON.parse(layoutString);
    console.log('\n  ✅ WASM output is valid JSON');
    console.log('  Layout object structure:');
    console.log('    - blocks:', Array.isArray(layoutObj.blocks) ? layoutObj.blocks.length : 'not array');
    if (layoutObj.blocks && layoutObj.blocks[0]) {
      console.log('    - First block keys:', Object.keys(layoutObj.blocks[0]));
      console.log('    - First block:', JSON.stringify(layoutObj.blocks[0], null, 2));
    }
  } catch (e) {
    console.log('\n  ❌ WASM output is NOT valid JSON:', e.message);
    console.log('  Attempting to decode as base64...');
    try {
      const decoded = fromBase64(layoutString);
      console.log('  Decoded length:', decoded.length);
      const decodedStr = new TextDecoder().decode(decoded);
      console.log('  Decoded content:', decodedStr.substring(0, 200) + '...');
    } catch (e2) {
      console.log('  Not base64 either:', e2.message);
    }
  }

  // Compact the JSON (remove whitespace)
  console.log('\n📝 COMPACTING LAYOUT JSON:');
  const compactLayoutJSON = JSON.stringify(layoutObj);
  const compactLayoutBytes = new TextEncoder().encode(compactLayoutJSON);
  const layoutBytesB64 = toBase64(compactLayoutBytes);

  console.log('  Original WASM length:', layoutBytes.length);
  console.log('  Compact JSON length:', compactLayoutJSON.length);
  console.log('  Compact bytes length:', compactLayoutBytes.length);
  console.log('  Base64 length:', layoutBytesB64.length);
  console.log('  Base64 preview:', layoutBytesB64.substring(0, 50) + '...');
  console.log('  Starts with KLUv/ (zstd magic)?', layoutBytesB64.startsWith('KLUv/'));

  // Simulate a signature (in real scenario this comes from wallet)
  const mockLayoutSignature = 'MOCK_LAYOUT_SIGNATURE_BASE64_STRING_HERE';

  console.log('\n🔑 GENERATING LAYOUT IDs:');
  const rq_ids_ic = 6; // Fixed value matching Go SDK
  const rq_ids_max = 10000; // Example max

  console.log('  Input to generateIds:');
  console.log('    layoutBytesB64:', layoutBytesB64.substring(0, 50) + '...');
  console.log('    layoutSignature:', mockLayoutSignature);
  console.log('    ic:', rq_ids_ic);
  console.log('    max:', rq_ids_max);

  const layoutIds = await generateIds(
    layoutBytesB64,
    mockLayoutSignature,
    rq_ids_ic,
    rq_ids_max
  );

  console.log('  Generated layout IDs count:', layoutIds.length);
  console.log('  First 3 IDs:', layoutIds.slice(0, 3));
  console.log('  Last ID:', layoutIds[layoutIds.length - 1]);

  // Build index file
  console.log('\n📄 BUILDING INDEX FILE:');
  const indexFile = buildIndexFile(layoutIds, mockLayoutSignature);
  console.log('  Index file structure:', JSON.stringify(indexFile, null, 2));

  const indexFileBytes = toCanonicalJsonBytes(indexFile);
  const indexFileB64 = toBase64(indexFileBytes);
  const indexFileString = new TextDecoder().decode(indexFileBytes);

  console.log('  Index file JSON:', indexFileString);
  console.log('  Index file base64 length:', indexFileB64.length);
  console.log('  Index file base64 preview:', indexFileB64.substring(0, 50) + '...');

  // Generate index IDs
  console.log('\n🆔 GENERATING INDEX IDs:');
  const mockIndexSignature = 'MOCK_INDEX_SIGNATURE_BASE64_STRING_HERE';
  const indexWithSignature = `${indexFileB64}.${mockIndexSignature}`;

  console.log('  Combined input length:', indexWithSignature.length);
  console.log('  Combined input preview:', indexWithSignature.substring(0, 100) + '...');

  const indexIds = await generateIdsFromCombined(
    indexWithSignature,
    rq_ids_ic,
    rq_ids_max
  );

  console.log('  Generated index IDs count:', indexIds.length);
  console.log('  First 3 IDs:', indexIds.slice(0, 3));
  console.log('  Last ID:', indexIds[indexIds.length - 1]);

  // Save outputs for comparison
  console.log('\n💾 SAVING DEBUG FILES:');

  fs.writeFileSync('/tmp/debug-layout-raw.json', layoutString);
  console.log('  Saved raw WASM output to /tmp/debug-layout-raw.json');

  fs.writeFileSync('/tmp/debug-layout-compact.json', compactLayoutJSON);
  console.log('  Saved compact layout to /tmp/debug-layout-compact.json');

  fs.writeFileSync('/tmp/debug-index.json', indexFileString);
  console.log('  Saved index file to /tmp/debug-index.json');

  const debugInfo = {
    testFile: {
      content: testContent,
      length: fileBytes.length,
      hash: dataHash
    },
    layout: {
      wasmOutputLength: layoutBytes.length,
      compactLength: compactLayoutJSON.length,
      base64Length: layoutBytesB64.length,
      base64: layoutBytesB64,
      signature: mockLayoutSignature,
      layoutIds: layoutIds
    },
    index: {
      jsonLength: indexFileString.length,
      base64Length: indexFileB64.length,
      base64: indexFileB64,
      signature: mockIndexSignature,
      indexIds: indexIds
    },
    config: {
      rq_ids_ic: rq_ids_ic,
      rq_ids_max: rq_ids_max
    }
  };

  fs.writeFileSync('/tmp/debug-info.json', JSON.stringify(debugInfo, null, 2));
  console.log('  Saved complete debug info to /tmp/debug-info.json');

  console.log('\n✅ TEST COMPLETE');
  console.log('\nCompare these outputs with Go SDK to find differences!');
}

// Run the test
testLayoutGeneration().catch(console.error);